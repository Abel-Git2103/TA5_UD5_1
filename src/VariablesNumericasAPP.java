
public class VariablesNumericasAPP {

	public static void main(String[] args) {
		
		int variableA = 19, variableB = 20;
		
		if(variableA > variableB) {
			System.out.println("La variable A es mayor que la variable B");
		}else if(variableA < variableB) {
			System.out.println("La variable A es menor que la variable B");
		}else {
			System.out.println("La variable A es igual a la variable B");
		}
	}
}
